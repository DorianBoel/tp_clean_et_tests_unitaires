package tp_nettoyage.ex3.interfaces;

/**
 * Interface to be implemented by any enumeration
 * with members labeled by a {@code String} value.
 *
 * @author DorianBoel
 */
public interface Labeled {

	/**
	 * Getter for the enum's label.
	 * 
	 * @return The enum's label
	 */
	String getLabel();
	
}
